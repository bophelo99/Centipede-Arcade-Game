#ifndef OBJECTSDIRECTION_H
#define OBJECTSDIRECTION_H


/** \enum objectsDirection
 *  \brief A strongly typed enum class representing the directions game
 *         objects could be facing. Used in the game to indicate which direction
 *         game objects are facing.
 *  \author 1932442 Bophilo Ntsemi and 1839578 Masilo Sekhele
 *  \version 3.0
 */
enum class objectsDirection
{
    UP=0,
    DOWN,
    LEFT,
    RIGHT,
    NONE
};
#endif // OBJECTSDIRECTION_H
